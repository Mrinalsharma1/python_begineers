# example of multiple value in multiple variable in one line
x, y, z = "orange", "apple", "guava"
print(x)
print(y)
print(z)

# example of single value in multiple variable
x = y = z = "orange"
print(x)
print(y)
print(z)

# unpack the list
fruits = ["apple", "banana", "guava"]
x, y, z = fruits
print(x)
print(y)
print(z)
